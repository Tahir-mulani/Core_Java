/*
Q4.Write a Java program to display Integer.MAX_VALUE and Integer.MIN_VALUE constants.
Explanation:
All wrapper classes (Integer, Double, Byte, etc.) have predefined constants for
maximum and minimum values.
This demonstrates how to use built-in constants instead of hardcoding limits.
*/
 
public class ConstantsValueAPP
{
	public static void main(String x[])
	{
		System.out.println("Integer Max value "+Integer.MAX_VALUE);
		System.out.println("Integer Min value "+Integer.MIN_VALUE);
		
		System.out.println("Double Max value "+Integer.MAX_VALUE);
		System.out.println("Double Min value "+Integer.MIN_VALUE);
		
		System.out.println("Byte Max value "+Byte.MAX_VALUE);
		System.out.println("Byte Min value "+Byte.MIN_VALUE);
		
		
	}
}


		 