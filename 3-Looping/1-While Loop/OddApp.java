//5.Write a C program to print all odd numbers between 1 to 100.
import java.util.*;
public class OddApp
{
 public static void main(String x[])
 {
  System.out.println("All Odd number from 1 to 100");
  int n=100;
  int i=1;
  while(i<=n)
  {
	if(i%2!=0)
    {
	 System.out.println(i);   
    }
   i++;
   }
  }
 }

   